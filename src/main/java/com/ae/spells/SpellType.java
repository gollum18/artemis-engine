package com.ae.spells;

/**
 * Helper enum that represents the various spell types.
 * <code>INT</code> represents Spells that are cast through the Intelligence stat.
 * <code>FAITH</code> represents Spells that are cast through the Faith stat.
 * <code>NECROTIC</code> represents Spells that are cast through both the Intelligence and Faith stats.
 */
public enum SpellType {
    INT,
    FAITH,
    FIRE,
    NECROTIC
}
