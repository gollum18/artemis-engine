package com.ae.resources;

/**
 * Helper enum that makes retrieving resources easier.
 * <code>ST_SPELLS</code> represents the spell static resource.
 * <code>ST_SPELLBOOKS</code> represents the spellbook static resource.
 * <code>ST_ARMOR</code> represents the armor static resource.
 * <code>ST_WEAPONS</code> represents the weapon static resource.
 * <code>ST_POTIONS</code> represents the potion static resource.
 * @author Christen Ford
 * @since 08/28/2020
 */
public enum ResourceType {
    RS_COMBAT,
    RS_DUNGEONS,
    RS_OVERWORLD,
    RS_SAFEZONES,
    ST_CREATURES,
    ST_NPCS,
    ST_SPELLS,
    ST_SPELLBOOKS,
    ST_ARMOR,
    ST_WEAPONS,
    ST_POTIONS,
    ST_SCROLLS,
    ST_FACTIONS,
    ST_ATTACKS
}
